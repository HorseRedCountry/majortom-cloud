/**
 * <p>
 *
 * </P>
 *
 * @author Major Tom
 * @since 2022/6/27 22:14
 */
@SpringBootApplication
@EnableEurekaServer
public class EurekaMain {
    public static void main(String[] args) {
        SpringApplication.run(EurekaMain.class, args);
    }
}
